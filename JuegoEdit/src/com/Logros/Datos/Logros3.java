    public void refrescarTabla(){
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        for(Usuario usuario: daoUser.logro3()){
            Object fila[]=new Object[2];
            fila[0]=usuario.getNombre();
            fila[1]=usuario.getUser();
            model.addRow(fila);
        }
        tablaUsuario3.setModel(model);
    }

